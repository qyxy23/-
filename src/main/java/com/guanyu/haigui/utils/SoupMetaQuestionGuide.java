package com.guanyu.haigui.utils;

/**
 * 海龟汤圈子元问题（本格/变格、清汤/红汤/黑汤）的 AI 判题参考。
 * 供主持 system prompt 与用户问题 prompt 共用，不替代每篇汤的 aiJudgeRules。
 */
public final class SoupMetaQuestionGuide {

    private SoupMetaQuestionGuide() {
    }

    /** 写入 AI system prompt 的简短版 */
    public static String systemPromptSection() {
        return "玩家可能问本格/变格、清汤/红汤/黑汤等元问题：只答是/不是/是或不是/不重要，"
                + "不得剧透汤底细节；优先依据汤底事实与判题规则，"
                + "并参考「清汤无死亡轻恐怖、红汤有死亡或强悬疑、黑汤重口高恐怖、"
                + "本格可现实解释、变格含超自然科幻灵异穿越」等圈子惯例。";
    }

    /** 写入单次判题 user prompt 的任务说明 */
    public static String userPromptTaskSection() {
        return """
                6. 元问题（本格/变格、清汤/红汤/黑汤，开局常问）：
                   - 只根据汤底与上方判题规则判断，不得透露具体真相、动机、人名情节。
                   - 本格：现实逻辑可解释，无鬼怪、超能力、穿越、科幻、灵异等 → 「是本格吗」按此答。
                   - 变格：含上述超自然或科幻设定 → 「是变格吗」按此答；与本格互斥。
                   - 清汤：通常无死亡、无血腥重口、恐怖负担较低。
                   - 红汤：涉及死亡、强悬疑/惊悚氛围（未必血腥）。
                   - 黑汤：重口、极高恐怖或烧脑复杂度。
                   - 「是清汤吗」「是红汤吗」「是黑汤吗」：分别独立判断，各答是/不是。
                   - 「是清汤还是红汤」类二选一：理解为「本篇是否更偏红汤（相对清汤）」——偏红汤答「是」，偏清汤答「不是」，两者都不明显答「是或不是」。
                   - 「是本格还是变格」：理解为「是否更偏变格」——变格答「是」，本格答「不是」，模糊答「是或不是」。
                   - 若判题规则与以上惯例冲突，以判题规则为准。
                """;
    }

    /** UGC 生成 aiJudgeRules 时要求写入的段落标题提示 */
    public static String ugcRulesRequirement() {
        return """
                - 必须包含「元问题判题」小节（约 80~150 字），明确写出本篇：
                  · 本格还是变格（依据汤底有无超自然/科幻/穿越等）
                  · 清汤/红汤/黑汤倾向（依据有无死亡、恐怖/重口程度；三者可说明最接近哪一类）
                  · 玩家问「是清汤吗」「是红汤吗」「是本格吗」「是变格吗」时分别答什么
                  · 提醒：只答是/否/不重要，不要解释剧情
                """;
    }
}
